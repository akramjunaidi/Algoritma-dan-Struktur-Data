# buatlah class
class luas:
    def __init__(self, sisi, luas):
        self.sisi = sisi
        self.luas = luas

    def hitung(self):
        return self.sisi + self.luas
    
class volume: 
    def __init__(self, tinggi, lebar):
        self.tinggi = tinggi
        self.lebar = lebar

    def hitung(self):
        return self.tinggi + self.lebar 